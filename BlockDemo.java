/*
	block of code in java
*/

class BlockDemo{
	public static void main(String[] args){
		double i, j, d;
		i = 5;
		j = 10;
		
		System.out.println("variables declared and defined: i = " + i + " and j = " + j);
		System.out.println();

		if (i != 0){
			System.out.println("variable i != 0");
			d = j / i;
			System.out.println("j / i is " + d);
		}
	}
}
